    '            if r.status_code == 200:\n'
    '                print(f"Model \'{model}\' works with /api/chat")\n'
    '                return model, "chat"\n'
    '        except Exception:\n'
    '            pass\n'
    '        try:\n'
    '            r = requests.post(f"{OLLAMA_BASE}/api/generate",\n'
