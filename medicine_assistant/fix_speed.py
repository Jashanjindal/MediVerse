        old_src = "".join(cell["source"])
        new_src = old_src.replace('"k": 8', '"k": 4')
        cell["source"] = src_to_lines(new_src)
        cell["outputs"] = []
        cell["execution_count"] = None
        print("Cell 2 patched: k=8 -> k=4")
        break

